using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace DivisionAndMultiplication
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // Declare and initialize two integer variables.
            int number1 = 50;
            int number2 = 10;

            // Multiply 'number1' and 'number2' and store the result in 'result1'.
            int result1 = number1 * number2;

            // Declare and initialize two more integer variables.
            int number3 = 50;
            int number4 = 10;

            // Divide 'number3' by 'number4' and store the result in 'result2'.
            int result2 = number3 / number4;

            // Print the results of the multiplication and division operations.
            Console.WriteLine("Result of multiplication: " + result1);
            Console.WriteLine("Result of division: " + result2);
            /*
             In this C# program, we perform two mathematical operations: multiplication and division. Here's a detailed explanation:

            We declare and initialize two integer variables, number1 and number2, with values 50 and 10, respectively.

            We use the multiplication operator (*) to multiply number1 and number2, and store the result in the variable result1.

            Then, we declare and initialize two more integer variables, number3 and number4, with the same values of 50 and 10.

            We use the division operator (/) to divide number3 by number4, and store the result in the variable result2.

            Finally, we print the results of both the multiplication and division operations to the console.

            This program demonstrates the use of basic arithmetic operators in C# to perform multiplication and division.
            It calculates the results and prints them to the console.
             */

        }
    }
}
